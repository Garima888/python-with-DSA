# One-Dimensional Array
arr = [10, 20, 30, 40, 50]

# Accessing elements
print(arr[0])  # Output: 10

# Inserting an element (at the end)
arr.append(60)  # Now arr = [10, 20, 30, 40, 50, 60]

# Deleting an element
arr.remove(30)  # Now arr = [10, 20, 40, 50, 60]

# Traversing the array
for element in arr:
    print(element)  # Output: 10, 20, 40, 50, 60
